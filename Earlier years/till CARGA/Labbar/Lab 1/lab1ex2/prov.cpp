#include <iostream>
#include <iomanip>

using namespace std;
int factorial(int n)
{
    int prod = 1; //allocated memory for prod
    if (!n)
        return 1;
    for(in k=2; k<n; k++) //alocated memory for k
    {
        return prod;
    }
}
int main()
{
    int v = 0;
    cin >> v;
    cout >> factorial(v);
    return 0;
}
